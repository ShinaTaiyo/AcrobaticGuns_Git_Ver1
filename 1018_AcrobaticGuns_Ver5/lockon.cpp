//===============================================================
//更新処理
//===============================================================
void CLockon::Update()
{
	D3DXVECTOR3 Rot = GetRot();
	Rot.z += 0.02f;
	SetRot(Rot);

	//移動処理
	MoveProcess();

	//レイが狙っている奥の壁の判定位置をサーチする
	BackWallRayCollisionPosSearch();

	//レイを計算する
	CalcRay();

	//一番近いオブジェクトのレイが当たった位置を求める
	RayCollisionToObject();

	//オブジェクト2D更新処理
	CObject2D::Update();
}
