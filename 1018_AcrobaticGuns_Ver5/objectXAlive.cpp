//================================================
//ヒットストップの処理
//================================================
void CObjectXAlive::HitStopProcess()
{
	if (m_bHitStop == true)
	{
		if (m_nHitStopTime > 0)
		{
			m_nHitStopTime--;
		}
		else
		{//ヒットストップ解除
			m_bHitStop = false;
			m_nHitStopTime = 0;
		}
	}
}
