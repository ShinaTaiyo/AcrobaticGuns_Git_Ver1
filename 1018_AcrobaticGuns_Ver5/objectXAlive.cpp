//============================================================
//更新処理
//============================================================
void CObjectXAlive::Update()
{
	//==============================================
    //体力を減らす
    //==============================================
	if (m_bAutoSubLife == true)
	{
		m_nLife--;
	}
	//===========================================================

	//==============================================
	//体力の割合で色合いの透明度を変える
	//==============================================
	if (m_bUseRatioLifeAlpha == true)
	{
		float fRatio;
		fRatio = float(m_nLife) / float(m_nMaxLife);
		SetColor(D3DXCOLOR(0.0f, 0.0f, 0.0f, fRatio), 3, true, true);
	}
	//===========================================================

	//==============================================
	//ヒットストップ処理
	//==============================================
	HitStopProcess();
	//===========================================================

	//==============================================
	//死亡フラグ発動処理（体力がなくなったら）
	//==============================================
	if (m_nLife < 1 && m_bAutoDeath == true)
	{
		SetDeath();
	}
	//===========================================================

	//親クラスの更新処理
	CObjectXMove::Update();

}
