//============================================================
//初期化処理
//============================================================
HRESULT CObjectXAlive::Init()
{
	//===========================================
   //体力関係
   //===========================================
	m_nLife = 1;                                        //体力
	m_nMaxLife = 1;                                     //最大体力
	//==================================================================================

	//親クラスの初期化処理
	CObjectXMove::Init();
	return S_OK;
}
